package gov.lanl.util.sru;

public class SRUValuePair {
    private String field;

    private String value;

    /**
     * Creates a new ValuePair 
     * @param field
     *        Element from Key enum
     * @param value
     *        String value associated with Key
     */
    public SRUValuePair(String field, String value) {
        this.field = field;
        this.value = value;
    }

    /**
     * Gets the Key's field name
     */
    public String getField() {
        return field;
    }

    /**
     * Gets the key's value
     */
    public String getValue() {
        return value;
    }
}
