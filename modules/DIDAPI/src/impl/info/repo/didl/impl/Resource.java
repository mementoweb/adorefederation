package info.repo.didl.impl;

/**
 * Provides a Resource type instance of an <code>Asset</code>.
 * 
 * @author Kjell Lotigiers <kjell.lotigiers@ugent.be>
 * @author Patrick Hochstenbach <patrick.hochstenbach@ugent.be>
 * @author Xiaoming Liu <liu_x@lanl.gov>
 * @see Asset
 */
public class Resource extends Asset {
    
    /**
     * Creates a new Resource instance
     */
	public Resource() {
        super(Asset.Type.RESOURCE);
    }
    
    /**
     * Creates a new Resource instance with associated identifier
     * @param id identifier of instance
     */
    public Resource(final String id) {
        super(Asset.Type.RESOURCE, id);
    }
}
